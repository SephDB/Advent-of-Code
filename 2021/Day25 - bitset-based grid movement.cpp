template<int N, typename F>
auto split_known(std::string_view in, char delim, F&& apply) {
    std::array<decltype(apply(in)),N> ret;
    std::size_t pos = 0;
    std::size_t current = 0;
    while((pos = in.find(delim)) != std::string_view::npos) {
        ret[current++] = apply(in.substr(0,pos));
        in.remove_prefix(pos+1);
    }
    ret[current++] = apply(in);
    return ret;
}

template<int W, int H>
struct Grid {
    std::array<std::bitset<W>,H> east{};
    std::array<std::bitset<W>,H> south{};

    Grid next(bool& moved_any) const {
        moved_any = false;
        Grid n{};
        //East
        for(int y = 0; y < H; ++y) {
            auto blocking = east[y] | south[y];
            
            auto blocked = (blocking >> 1);
            blocked.set(W-1,blocking.test(0));
            blocked = blocked & east[y];
            
            auto moved = (east[y] << 1);
            moved.set(0,east[y].test(W-1));

            auto allow_move = moved & (~blocking);
            moved_any = moved_any || allow_move.any();
            n.east[y] = blocked | allow_move;
        }

        //South
        for(int y = 0; y < H; ++y) {
            int next_y = (y+1)%H;
            auto blocking = n.east[next_y] | south[next_y];
            n.south[y] |= south[y] & blocking;
            auto moved = south[y] & (~blocking);
            moved_any = moved_any || moved.any();
            n.south[next_y] |= moved;
        }

        return n;
    }

    friend std::ostream& operator<<(std::ostream& o, const Grid& g) {
        for(int y = 0; y < H; ++y) {
            for(int x = 0; x < W; ++x) {
                if(g.east[y].test(x)) o << '>';
                else if(g.south[y].test(x)) o << 'v';
                else o << '.';
            }
            o << '\n';
        }
        return o;
    }
};

template<int W, int H>
auto parse(std::string_view input) {
    Grid<W,H> parsed{};
    auto lines = split_known<H>(input,'\n',[](std::string_view line) {
        std::array<std::bitset<W>,2> out{};
        for(int i = 0; i < W; ++i) {
            if(line[i] == 'v') out[0].set(i);
            if(line[i] == '>') out[1].set(i);
        }
        return out;
    });
    for(int y = 0; y < H; ++y) {
        parsed.south[y] = lines[y][0];
        parsed.east[y] = lines[y][1];
    }
    return parsed;
}

