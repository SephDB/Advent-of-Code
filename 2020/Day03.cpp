#include <iostream>
#include <string_view>
#include <array>
#include <numeric>
#include <complex>
#include <charconv>

struct Grid {
    std::string_view grid;
    std::size_t width;
    std::size_t height;
    Grid(std::string_view g, std::size_t w) : grid(g), width(w), height(g.size()/w) {}
    char operator[](std::complex<int> loc) const {
        return grid[loc.imag()*(width+1) + (loc.real() % width)];
    }
};

auto parse(std::string_view input) {
    return Grid(input,input.find('\n'));
}

int num_trees(Grid g, std::complex<int> dir) {
    int count = 0;
    for(std::complex<int> current = {0,0}; current.imag() < g.height; current += dir) {
        count += g[current] == '#';
    }
    return count;
}

auto part1(decltype(parse("")) input) {
    return num_trees(input,{3,1});
}

auto part2(decltype(parse("")) input) {
    std::array<std::complex<int>,5> dirs = {{{1,1},{3,1},{5,1},{7,1},{1,2}}};
    auto trees = [&input](auto dir) {return num_trees(input,dir);};
    return std::transform_reduce(dirs.begin(),dirs.end(),1ll,std::multiplies<>{},trees);
}

void solution(std::string_view input) {
    auto in = parse(input);
    std::cout << "Part 1: " << part1(in) << '\n';
    std::cout << "Part 2: " << part2(in) << '\n';
}

